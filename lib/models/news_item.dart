class NewsItem {
  final String id;
  final String title;
  final String imageUrl;
  final String category;
  final String date;
  final String summary;
  final String content;

  const NewsItem({
    required this.id,
    required this.title,
    required this.imageUrl,
    required this.category,
    required this.date,
    required this.summary,
    required this.content,
  });
}
